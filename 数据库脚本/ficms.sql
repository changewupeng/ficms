CREATE DEFINER=`root`@`localhost` FUNCTION `fnc_gecs_get_item_name`(in_CATEGORY_CDDE VARCHAR(32),in_item_code VARCHAR(32)) RETURNS varchar(64) CHARSET utf8
    DETERMINISTIC
    COMMENT '获取字典表中字典对应的实际值'
BEGIN
     DECLARE value VARCHAR(64);
     SET value = '';
     SELECT dmbmmc INTO value
          FROM sys_dmbm_dic
          WHERE category = in_CATEGORY_CDDE and dmbm=in_item_code;
     RETURN value;
END
